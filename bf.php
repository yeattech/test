                    <h2 id="bf1"><u>Explanation</u></h2>
                    <p>The Build Fighter series, sets in a version of our world where Gundam is the singular unifying global cultural touchstone. The story follows Sei Iori, a young Gunpla builder, whose parents own a model store. One day, he meets an odd red-headed foreigner named Reiji, who somehow has never even heard about Gundam, let alone the latest global craze, Gunpla Battling.</p>
                    <p>In the sport of Gunpla Battling, players use custom Gundam models to battle in holographic arenas. When exposed to the Plavsky Particle created by the Gundam battling system, Gunpla are able to move and use their weapons as if they were real, this means that build quality, loadout and customization are key the performance of Gunpla.</p>
                    <p>Sei is a Gundam aficionado, and a masterful builder and Reiji is a battling genius. Together, the two team up with Reiji fighting using Sei’s customized Gunpla, the Build Strike Gundam, aiming to win the Gunpla Battle World Championship. Along the way, they'll meet foes from across the world, learn more about themselves, and even discover the secrets behind the Plavsky Particle.</p>
                    <p>Obviously, the stakes are considerably lower than your average Gundam series. This allowed the producer to make a Gundam series that operates more as a comedy. For example, there is a combination beach slash onsen episode that goes roughly as one expected but then somehow ends with defeating onsen menacing yakuza in a gunpla battle.</p>
                    <p>This ridiculousness continuously escalates with Gunpla martial arts, Gunpla mafiosos and other ridiculous things. The whole premise is just ridiculous, yet it does take Gunpla very seriously. There's this notion that Gunpla is this singular profound craft that exceeds all others, so long as you're having fun which is itself ridiculous.</p>

                    <h4><u>Personal Thoughts</u></h4>
                    <p>So, is Build Fighters worth watching? Yes, it doesn't require any pre-existing Gundam knowledge to enjoy it, but you'll have a better time if you can pick up on all the Gundam cameos and goofs. Detached from Gundam, it's just a fun anime about two friends competing in a futuristic fighting game tournament scene.</p>
