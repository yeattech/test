					<h2 id="uc1"><u>Explanation</u></h2>
					<p>The Universal Century is the setting of the original Mobile Suit Gundam, and it is also by far the most developed and revisited to the Gundam timelines. Set in the future after mankind has created space colonies in orbit around the earth. Stories in the universal century mostly tell the tale of the wars between the Earth Federation, the one-world government, and Zeon a nation of rebelling space colonies. As time goes on both alternate between which plays the antagonists, mostly Zeon, though it often turns out that they both are.</p>
					<p>Much of the stories focus on the genesis of the space-born psychics, Newtype, who may have the potential for enabling humanity to move past war through achieving mutual understanding or maybe just escalate the war and horrifying new directions further. Further down in the Universal century chronology, the Zeon versus Federation dynamic is superseded by several stand-alone wars between people vaguely associated with the federation and other non-Zeon space-dwelling bad guys but, there are very few stories set in that bit and so who cares?</p>
